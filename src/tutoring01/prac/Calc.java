package tutoring01.prac;

public class Calc {
    public static void main(String[] args) {
        int a = 7;
        int b = 3;

        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a % b;

        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
